import { NextRequest, NextResponse } from 'next/server';

const CONFIG_SERVICE_URL = process.env.CONFIG_SERVICE_URL || 'http://localhost:8080';

export async function GET(request: NextRequest) {
  const token = request.cookies.get('incidentfox_session_token')?.value;

  if (!token) {
    return NextResponse.json({ error: 'Not authenticated' }, { status: 401 });
  }

  try {
    const category = request.nextUrl.searchParams.get('category') || '';
    const search = request.nextUrl.searchParams.get('search') || '';

    const params = new URLSearchParams();
    if (category) params.set('category', category);
    if (search) params.set('search', search);

    const queryString = params.toString();
    const url = `${CONFIG_SERVICE_URL}/api/v1/admin/templates${queryString ? `?${queryString}` : ''}`;

    const res = await fetch(url, {
      headers: { 'Authorization': `Bearer ${token}` },
    });

    const data = await res.json();
    return NextResponse.json(data, { status: res.status });
  } catch (e: any) {
    return NextResponse.json({ error: e?.message || 'Failed to fetch templates' }, { status: 500 });
  }
}
